class RelationalOperators
  {
    public static void main(String args[])
    {
     System.out.println("less than "+(23<89));
     System.out.println("less than equal to "+(25<=34));
     System.out.println("greater than "+(89>55));
     System.out.println("greater than equal to "+(89>=78));
     System.out.println("equal to "+(23==23));
     System.out.println("not equal to "+(23!=89)); 
    }
  }